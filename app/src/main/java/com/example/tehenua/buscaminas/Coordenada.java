package com.example.tehenua.buscaminas;

public class Coordenada {
    int i;
    int j;


    public Coordenada() {
    }

    public Coordenada(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
